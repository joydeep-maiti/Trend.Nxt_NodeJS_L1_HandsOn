const Addition = (a,b) => {
    return a+b;
}
const Subtraction = (a,b) => {
    return a-b;
}
const Multiplication = (a,b) => {
    return a*b;
}
const Division = (a,b) => {
    return a/b;
}

exports.Addition = Addition;
exports.Subtraction = Subtraction;
exports.Multiplication = Multiplication;
exports.Division = Division;